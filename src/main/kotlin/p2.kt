fun main() {
    //Integar to Double
    val a: Int = 10
    val b: Double = a.toDouble()
    println("Double value (from Integer):$b")

    //String to Int
    val c: String = "4456"
    val d: Int = c.toInt()
    println("Integer value (from string):$d ")

    //String to Double
    val e: String = "555"
    val f: Double = e.toDouble()
    println("Double value (from string):$f")
}